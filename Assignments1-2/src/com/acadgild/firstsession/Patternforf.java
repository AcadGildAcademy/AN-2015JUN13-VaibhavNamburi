package com.acadgild.firstsession;

public class Patternforf {

	public static void main(String[] args) {
		int i;
		int j;
		
		for(i=0 ; i<5 ; i++){
			for( j =i ; j>0; j--){
				System.out.print(" ");
			}
			System.out.print(" #");
			System.out.println();
			System.out.println();
		}
		
		
	}

}
